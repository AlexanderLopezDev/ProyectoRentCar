    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
                          String id = txtIdRecepcion.getText();

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Ingrese ID Recepción");
        return;
    }

    ManejoDeArchivo ma = new ManejoDeArchivo();
    String datos[] = ma.BuscarDatos(id, new File("recepcionVehiculos.txt"));

    if (datos != null) {
        // 🔵 EXISTE → MODIFICANDO
        habilitarCampos();

        txtIdMatricula.setText(datos[1]);
        txtFechaRecepcion.setText(datos[2]);
        txtObservacion.setText(datos[3]);

        txtIdMatricula.setEditable(false); // ❌ NO modificar
                              String lineaOriginal = String.join(";", datos);
        lbEstado.setText("MODIFICANDO");
                              boolean lbEstadoB = true;

    } else {
        // 🟢 NO EXISTE → CREANDO
        habilitarCampos();

        txtIdMatricula.setText("");
        txtFechaRecepcion.setText("");
        txtObservacion.setText("");

        txtIdRecepcion.setText(id);

        lbEstado.setText("CREANDO");
          boolean lbEstadoB = false;
    }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
                            File archivo = new File("recepcionVehiculos.txt");

    String id = txtIdRecepcion.getText();
    String matricula = txtIdMatricula.getText();
    String fecha = txtFechaRecepcion.getText();
    String obs = txtObservacion.getText();

    // 🔴 VALIDAR CAMPOS
    if (id.isEmpty() || matricula.isEmpty() || fecha.isEmpty() || obs.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios");
        return;
    }

    ManejoDeArchivo ma = new ManejoDeArchivo();

    // 🔴 VALIDAR MATRÍCULA EXISTE
    String datosVeh[] = ma.BuscarDatos(matricula, new File("vehiculos.txt"));

    if (datosVeh == null) {
        JOptionPane.showMessageDialog(this, "La matrícula no existe");
        return;
    }

    // 🔴 VALIDAR RESERVA
    String datosReserva[] = ma.BuscarDatos(matricula, new File("reservas.txt"));

    if (datosReserva == null) {
        JOptionPane.showMessageDialog(this, "No existe reserva para esta matrícula");
        return;
    }

    String fechaEntrada = datosReserva[2];

    if (fecha.compareTo(fechaEntrada) < 0) {
        JOptionPane.showMessageDialog(this, "Fecha inválida");
        return;
    }

    String datos = id + ";" + matricula + ";" + fecha + ";" + obs;
        boolean lbEstadoB = false;

    if (lbEstadoB) {
         String lineaOriginal = null;
        ma.ModificarDatos(lineaOriginal, datos, archivo);
        JOptionPane.showMessageDialog(this, "Modificado correctamente");
    } else {
        ma.GuardarDatos(datos, archivo);
        JOptionPane.showMessageDialog(this, "Guardado correctamente");
    }

    // 🔥 ACTUALIZAR VEHÍCULO
    actualizarVehiculoDisponible(matricula);

    limpiarDatos();
    bloquearCampos();
    }//GEN-LAST:event_btnGuardarActionPerformed

