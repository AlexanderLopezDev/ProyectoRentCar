    private void bloquearCampos() {
    txtIdRecepcion.setEditable(true);
    txtIdMatricula.setEditable(false);
    txtFechaRecepcion.setEditable(false);
    txtObservacion.setEditable(false);
    btnGuardar.setEnabled(false);
    lbEstado.setEnabled(false);
    }

   public void habilitarCampos() {
    txtIdRecepcion.setEditable(false);
    txtIdMatricula.setEditable(true);
    txtFechaRecepcion.setEditable(true);
    txtObservacion.setEditable(true);
    btnGuardar.setEnabled(true);
    lbEstado.setEnabled(true);
}
